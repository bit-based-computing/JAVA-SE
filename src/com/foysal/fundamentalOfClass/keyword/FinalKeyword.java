package com.foysal.fundamentalOfClass.keyword;

public class FinalKeyword {
    //A field can be declared as final. Doing so prevents its contents from being modified,
    //making it, essentially, a constant. This means that you must initialize a final field
    //when it is declared. You can do this in one of two ways: First, you can give it a value
    //when it is declared. Second, you can assign it a value within a constructor.
    //The first approach is the most common.

    final String FIRST_NAME = "FOYSAL";
    final String LAST_NAME = "AHMMED";

}
